// folds the (l2 / QK_POLAR) compensation into the per-chunk scale at
// load time. Bit-equivalent to ggml_vec_dot_q4_polar_q8_0 modulo
// floating-point reordering. See ggml/src/ggml-cpu/fused-q4-polar-dot.c
